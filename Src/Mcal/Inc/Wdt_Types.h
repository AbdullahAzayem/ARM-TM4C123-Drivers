#ifndef WDT_TYPES_H_
#define WDT_TYPES_H_

/**********************************************************************************************************************
 * INCLUDES
 *********************************************************************************************************************/
#include "Std_Types.h"
#include "Mcu_Hw.h"


/**********************************************************************************************************************
 *  GLOBAL CONSTANT MACROS
 *********************************************************************************************************************/
 /*WDT Standard or Non maskable interrupt*/
 typedef uint8 					Wdt_InterruptType;
 #define WDT_NMI 						((uint32*)1)
 #define WDT_STD						((uint32*)0)
  
  
  
/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/
 
/*Configuration of watchdog timer*/ 
typedef struct 
{
	void  	   			    (*NotificationEN)  (void); 	    /*Notification enable: WDT_Enable or WDT_Disable*/
	Wdt_InterruptType 		  InterruptType;  			    /*WDT_STD, WDT_NMI*/
	const uint16					  WdtInitialTimeout_ms;			/*initial time out value*/
	uint16 		 			  WdtMaxTimeout_ms;    			/*Maximum timeout value*/
}Wdt_ConfigType;
 

#endif  /* WDT_TYPES_H_ */

